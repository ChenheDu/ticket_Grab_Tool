package src.main.java.com.github.ticketGrab.model;

public class Member {

    private Integer id;
    private String name;
    private Integer idCardNo;

    public Integer getId() {
        return this.id;
    }

    public void setId(final Integer id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public Integer getIdCardNo() {
        return this.idCardNo;
    }

    public void setIdCardNo(final Integer idCardNo) {
        this.idCardNo = idCardNo;
    }
}
